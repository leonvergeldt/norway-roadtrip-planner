function categoryDetail(highlight: Highlight) {
  const categoryText: Record<Category, string> = {
    city: `${highlight.name} werkt als praktische pauze in de route: voorzieningen dichtbij, vaak wat cultuur, en genoeg keuze zonder een hele dag vast te leggen.`,
    fjord: `${highlight.name} draait om kijken, stoppen en vertragen. Water, wanden en licht bepalen hier vaak meer dan een strak plan.`,
    hike: `${highlight.name} verdient alleen een plek in de dag als weer, energie en timing kloppen. Plan ruimte voor omkeren, pauzeren en rustig aankomen.`,
    stave_church: `${highlight.name} brengt een korte cultuurstop in het landschap: meestal weinig logistiek, maar wel duidelijk Noors en historisch eigen.`,
    kayak: `${highlight.name} is een keuze voor nabijheid tot het water. Als weer en energie kloppen, kan dit een ontspannen manier zijn om een fjord te beleven.`,
    viewpoint: `${highlight.name} is vooral waardevol bij zicht. Houd het flexibel: stoppen als de lucht open is, doorrijden als wolken alles dichttrekken.`,
    scenic_route: `${highlight.name} is een routekeuze, geen afvinkpunt. Laat ruimte voor fotostops, kleine omwegen en langzaam rijden.`,
  };

  return categoryText[highlight.category];
}

function buildDetail(highlight: Highlight) {
  if (customDetails[highlight.id]) return customDetails[highlight.id];

  return [
    categoryDetail(highlight),
    highlight.note ??
      `In de regio ${highlight.region} is dit vooral logisch als flexibele stop: kort genoeg om ter plekke te beslissen, maar duidelijk genoeg om richting aan de dag te geven.`,
  ];
}

